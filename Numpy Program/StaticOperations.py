import numpy as np

# Define an array
array = np.array([10, 20, 30, 40, 50])

# Calculate the required statistical operations
mean = np.mean(array)
maximum = np.max(array)
minimum = np.min(array)
total_sum = np.sum(array)

# Display results
print("Array:")
print(array)
print("\nMean of the array:", mean)
print("Maximum value in the array:", maximum)
print("Minimum value in the array:", minimum)
print("Sum of all elements in the array:", total_sum)
